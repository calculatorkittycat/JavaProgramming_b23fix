package day03_Variables;

/*
\n New Line
\t Paragraph Space (tab)
\" this is how you allow a quotation mark to be seen
\\ show a backslash
\\\\ show a double back slash
 */

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone \nhow are you all today?");
        System.out.println();

        System.out.println("Hello everyone \nmy name is Ivan \nI live in seattle");
        System.out.println();

        System.out.println("\tParagraph space \nYour mom is my moms uncles brother");
        System.out.println();

        System.out.println("Dick and jane \"ran up the hill\"");


    }



}
