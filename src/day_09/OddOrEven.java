package day_09;

public class OddOrEven {

    public static void main(String[] args) {


        int number = 99;

        if (number %2 != 0){ //if the number divided by 2 is not equal to 0
            System.out.println(number + " is odd number");
        }else{
            System.out.println(number + " is even number");
        }

    }
}
