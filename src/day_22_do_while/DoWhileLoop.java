public class DoWhileLoop {

    public static void main(String[] args) {
    int i = 10;

    while(i<5){//if false while loops will be executed no times
        System.out.println("Hello Cybtertonk");
    }
        System.out.println("_________________________________");
    do{//even if the condition is false do while loop will be executed once
        System.out.println("Hello Cybertonk");
    }while(i<5);
    }

}
