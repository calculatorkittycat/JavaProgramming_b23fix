package day_23_Arrays;

public class NestedLoop {

    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");

        }
        System.out.println();

        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");

        }
        System.out.println();

        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");

        }
        System.out.println();

        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");

        }
        System.out.println();

        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");

        }
        System.out.println();


        System.out.println("______________________________");

        for (int j = 1; j <= 5; j++) {
            for (int i = 1; i <= 5; i++) {
                System.out.print(i + " ");
            }
            System.out.println();
        }
    }
}

/*
nested loop: loop inside another loop
there will be an inner loop and an outer loop
used whenever you need to repeat a loop
 */